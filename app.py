# --- Upload Mode Tabs ---
tab1, tab2 = st.tabs(["Single Image", "Batch Upload"])
client = genai.Client(api_key=st.session_state.api_key)

# --- Single Image Tab ---
with tab1:
    uploaded_file = st.file_uploader("Upload an image", type=["jpg", "jpeg", "png", "webp"], key="single")
    if uploaded_file:
        image = Image.open(uploaded_file)
        st.image(image, use_container_width=True)
        if st.button("Annotate Image", key="annotate_single"):
            with st.spinner("Annotating..."):
                try:
                    result = annotate_image(client, selected_model, image_to_bytes(image))
                    entry = {
                        "image_id": len(st.session_state.dataset) + 1,
                        "filename": uploaded_file.name,
                        "annotations": result
                    }
                    st.session_state.dataset.append(entry)

                    st.subheader("Annotation Results")
                    st.write(f"**Dense Caption:** {result['dense_caption']}")
                    st.write(f"**Scene:** {result['scene']} | **Lighting:** {result['lighting']}")
                    st.write(f"**Objects:** {', '.join(result['objects'])}")
                    st.write("**VQA Pairs:**")
                    for pair in result.get("vqa_pairs", []):
                        st.write(f"- Q: {pair['question']}")
                        st.write(f"  A: {pair['answer']}")
                    if result.get("confidence_notes"):
                        st.caption(f"Notes: {result['confidence_notes']}")
                    with st.expander("Raw JSON"):
                        st.json(result)
                    st.success(f"Added to dataset. Total: {len(st.session_state.dataset)} images.")
                except Exception as e:
                    if "429" in str(e) or "RESOURCE_EXHAUSTED" in str(e):
                        st.error("Rate limit hit. Try switching to a different model in the sidebar.")
                    else:
                        st.error(f"Something went wrong: {e}")

# --- Batch Upload Tab ---
with tab2:
    uploaded_files = st.file_uploader(
        "Upload multiple images",
        type=["jpg", "jpeg", "png", "webp"],
        accept_multiple_files=True,
        key="batch"
    )
    if uploaded_files:
        st.write(f"{len(uploaded_files)} image(s) selected.")
        if st.button("Annotate All", key="annotate_batch"):
            progress = st.progress(0, text="Starting...")
            errors = []
            for i, f in enumerate(uploaded_files):
                progress.progress((i) / len(uploaded_files), text=f"Annotating {f.name}...")
                try:
                    image = Image.open(f)
                    result = annotate_image(client, selected_model, image_to_bytes(image))
                    st.session_state.dataset.append({
                        "image_id": len(st.session_state.dataset) + 1,
                        "filename": f.name,
                        "annotations": result
                    })
                except Exception as e:
                    if "429" in str(e) or "RESOURCE_EXHAUSTED" in str(e):
                        errors.append(f"{f.name}: Rate limit hit — switch models and retry.")
                    else:
                        errors.append(f"{f.name}: {e}")
            progress.progress(1.0, text="Done!")
            st.success(f"Annotated {len(st.session_state.dataset)} images total.")
            if errors:
                st.warning("Some images failed:")
                for err in errors:
                    st.write(f"- {err}")

