for key, val in [("api_key", None), ("connected", False), ("dataset", [])]:
    if key not in st.session_state:
        st.session_state[key] = val

# --- API Key Modal ---
@st.dialog("Connect to Gemini")
def api_key_dialog():
    st.markdown("Enter your Gemini API key to get started.")
    st.markdown("[Get a free key from Google AI Studio](https://aistudio.google.com/app/apikey)", unsafe_allow_html=True)
    key = st.text_input("API Key", type="password", placeholder="AIza...")
    if st.button("Connect", width='stretch'):
        if key.strip():
            try:
                client = genai.Client(api_key=key.strip())
                client.models.generate_content(model="gemini-2.5-flash", contents="ping")
                st.session_state.api_key = key.strip()
                st.session_state.connected = True
                st.rerun()
            except Exception as e:
                st.error(f"Could not connect: {e}")
        else:
            st.warning("Please enter an API key.")

if not st.session_state.connected:
    api_key_dialog()
    st.stop()

# --- Annotation Prompt ---
ANNOTATION_PROMPT = """Analyze this image and return a JSON object with exactly these fields:

- dense_caption: A detailed 2-4 sentence description suitable for VLM training. Include objects, actions, spatial relationships, lighting, and context.
- objects: A list of all identifiable objects in the image.
- scene: Scene type (e.g. indoor, outdoor, aerial, medical, abstract).
- lighting: Lighting conditions (e.g. natural daylight, artificial, low light, overcast).
- vqa_pairs: A list of 3 question-answer pairs about the image. Each pair should be an object with "question" and "answer" fields.
- confidence_notes: Any uncertainty about the analysis.

Return only valid JSON with no markdown backticks."""

def annotate_image(client, model, image_bytes):
    response = client.models.generate_content(
        model=model,
        contents=[
            types.Part.from_bytes(data=image_bytes, mime_type="image/png"),
            ANNOTATION_PROMPT
        ]
    )
    raw = response.text.strip()
    if raw.startswith("```"):
        raw = raw.split("\n", 1)[1].rsplit("```", 1)[0].strip()
    return json.loads(raw)

def image_to_bytes(image):
    buffer = io.BytesIO()
    image.save(buffer, format="PNG")
    return buffer.getvalue()

# --- Export Functions ---
def export_jsonl(dataset):
    lines = []
    for entry in dataset:
        a = entry["annotations"]
        # One line per image with full annotation
        record = {
            "image_id": entry["image_id"],
            "filename": entry["filename"],
            "dense_caption": a.get("dense_caption", ""),
            "objects": a.get("objects", []),
            "scene": a.get("scene", ""),
            "lighting": a.get("lighting", ""),
            "vqa_pairs": a.get("vqa_pairs", []),
        }
        lines.append(json.dumps(record))
    return "\n".join(lines)

def export_csv(dataset):
    output = io.StringIO()
    writer = csv.writer(output)
    writer.writerow(["image_id", "filename", "dense_caption", "objects", "scene", "lighting", "confidence_notes"])
    for entry in dataset:
        a = entry["annotations"]
        writer.writerow([
            entry["image_id"],
            entry["filename"],
            a.get("dense_caption", ""),
            "; ".join(a.get("objects", [])),
            a.get("scene", ""),
            a.get("lighting", ""),
            a.get("confidence_notes", ""),
        ])
    return output.getvalue()

def export_coco(dataset):
    coco = {
        "info": {"description": "Generated by Vision Dataset Pipeline", "date_created": datetime.now().isoformat()},
        "images": [],
        "annotations": [],
        "categories": []
    }
    all_objects = set()
    for entry in dataset:
        for obj in entry["annotations"].get("objects", []):
            all_objects.add(obj)
    cat_map = {name: i+1 for i, name in enumerate(sorted(all_objects))}
    coco["categories"] = [{"id": v, "name": k} for k, v in cat_map.items()]

    ann_id = 1
    for entry in dataset:
        img_id = entry["image_id"]
        coco["images"].append({"id": img_id, "file_name": entry["filename"]})
        for obj in entry["annotations"].get("objects", []):
            coco["annotations"].append({
                "id": ann_id,
                "image_id": img_id,
                "category_id": cat_map[obj],
                "caption": entry["annotations"].get("dense_caption", "")
            })
            ann_id += 1
    return json.dumps(coco, indent=2)

def build_zip(dataset):
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w") as zf:
        zf.writestr("annotations.jsonl", export_jsonl(dataset))
        zf.writestr("annotations.csv", export_csv(dataset))
        zf.writestr("annotations_coco.json", export_coco(dataset))
    return buf.getvalue()

